% MATLAB script to calculate the sum of elements in a vector

% Define a vector v containing the numbers 1 through 5
v = 1:5;

% Calculate the sum of elements in the vector
sum_of_elements = sum(v);

% Display the result
disp(['Vector v: ', num2str(v)]);
disp(['Sum of elements in v: ', num2str(sum_of_elements)]);
